<?php
// Треба вивести на екран 10 рідків
// На всіх рядках має бути крапка
// На кратних 3 треба після крапки додати foo
// На кратних 7 треба після крапки додати bar

for($i = 1; $i <=10; $i++){

    $row = '.';

    if ($i % 3 === 0){
        echo $row . 'foo';
    }

    if ($i % 7 === 0){
        echo $row . 'bar';
    }

    echo $row . PHP_EOL;
}

?>